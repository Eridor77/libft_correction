#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		ft_putstr("-214748648");
	else
	{
		if (n < 0)
		{
			ft_putchar_fd('-', 1);
			n = -n;
		}
		if (n >= 10)
		{
			ft_putnbr_fd(n / 10, 1);
			ft_putnbr_fd(n % 10, 1);
		}
		else
		{
			n += 48;
			write(fd, &n, 1);
		}
	}
}
